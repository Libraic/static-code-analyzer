package org.libra.model.token;

/**
 * A specific token type affiliated to each individual token.
 */
public enum TokenType {
    NUMERIC,
    STRING,
    ARITHMETIC_OPERATOR,
    ASSIGNMENT_OPERATOR,
    DATA_TYPE,
    VARIABLE_NAME,
    PARENTHESES,
    INSTRUCTION,
    PROGRAM,
    METHOD,
    ACCESS_MODIFIER,
    STATIC_ACCESS,
    STATE,
    RETURN_TYPE,
    METHOD_DECLARATION,
    COMMA_SEPARATOR,
    UNDEFINED
}
